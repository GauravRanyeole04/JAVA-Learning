class IfElse
{
	public static void main(String[] args) 
	{
		System.out.println(" Execution Start");
		if(false)
		{
			System.out.println(" Hello from If Block");
		}
		else
		{
			System.out.println(" Hello from Else Block");
		}
		System.out.println(" Execution End");
	}
}

